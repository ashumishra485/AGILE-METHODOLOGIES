
public class TempConvException extends Exception{
	public TempConvException ()
	   {
	      super ();
	   }

	   public TempConvException (String message)
	   {
	      super (message);
	   }

	   public TempConvException (Throwable cause)
	   {
	      super (cause);
	   }

	   public TempConvException (String message, Throwable cause)
	   {
	      super (message, cause);
	   }

}
